#include <unistd.h>

void	*ft_bzero(void *s, size_t n)
{
	size_t			i;
	unsigned char	*cc;

	i = 0;
	cc = s;
	while (i < n)
	{
		*cc = 0;
		i++;
		cc++;
	}
	return (s);
}
